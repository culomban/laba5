import math
def area(r):
    return math.pi * r * r
    '''
    Возвращает площадь круга

        Параметры:
            r (int): радиус круга

        Возвращаемое значение:
            int: площадь круга
    '''

def perimeter(r):
    return 2 * math.pi * r
    '''
    Возвращает периметр круга

        Параметры:
            r (int): радиус круга

        Возвращаемое значение:
            int: периметр
    '''
